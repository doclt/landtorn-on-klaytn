import { ethers, hardhatArguments } from 'hardhat'
import { initConfig, setConfig, updateConfig } from './ultil'

async function main() {
  await initConfig()
  const network = hardhatArguments.network ? hardhatArguments.network : 'dev'

  const torn = await ethers.deployContract('Torn')

  await torn.waitForDeployment()
  console.log(`torn with address: ${await torn.getAddress()}`)
  setConfig(`${network}.Torn`, await torn.getAddress())
  await updateConfig()
}

// We recommend this pattern to be able to use async/await everywhere
// and properly handle errors.
main().catch((error) => {
  console.error(error)
  process.exitCode = 1
})
